public class single_Inheritance {
      static class A {
            public static void func() {
                  System.out.println("Inherited");
            }
      }

      static class B extends A {
      }

      public static void main(String[] args) {
            B test = new B();
           B.func();
      }
}
                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                                 